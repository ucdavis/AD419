CREATE VIEW dbo.BenefitsDiversionV
AS
SELECT        FIN_COA_CD AS Chart, ACCOUNT_NBR AS Account, OP_FUND_NUM AS OpFundNum, FIN_OBJ_LEVEL_CD AS LevelCode, 
                         RPTS_TO_FIN_COA_CD AS ReportsToChart, RPTS_TO_ACCOUNT_NBR AS ReportsToAccount, RPTS_TO_SUB_ACCT_NBR AS ReportsToSubAccount, 
                         RPTS_TO_PROJECT_CD AS ReportsToProject, ACTIVE_IND AS ActiveInd, LAST_UPDATE_TS AS LastUpdateDate, ACCT_EXPIRATION_DATE AS ExpirationDate
FROM            OPENQUERY(FIS_DS, 
                         'SELECT *
		FROM 
		FINANCE.ORGANIZATION_ACCOUNT A 
		INNER JOIN FINANCE.BENEFIT_DIVERSION BD ON
			A.CHART_NUM  = BD.FIN_COA_CD AND
			A.ACCT_NUM = BD.ACCOUNT_NBR  
		WHERE 
			A.FISCAL_YEAR = 9999 AND
			A.FISCAL_PERIOD = ''--''

  ')
                          AS BD
GO
